struct Line {
  std::string value;
};

std::istream &operator>>(std::istream &is, Line &l) {
  std::getline(is, l.value);
  return is;
}

auto count(std::vector<Line> const &lines, int x_inc, int y_inc) {
  auto x = 0;
  size_t trees = 0;

  for (auto y = 0; y < lines.size(); y += y_inc) {
    auto const &line = lines[y].value;

    if (line[x % line.size()] == '#')
      trees += 1;

    x += x_inc;
  }

  return trees;
}

